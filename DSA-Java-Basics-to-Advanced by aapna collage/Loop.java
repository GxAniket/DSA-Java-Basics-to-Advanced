//WAP TO PRINT HELLO WORLD 100 TIMES USING LOOP
public class Loop {
    public static void main (String[]args) {
        //counter++ => counter=counter+1
        for (int counter =0; counter < 100 ; counter++){
            System.out.println("Hello World");
        }
    }
}

